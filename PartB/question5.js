// 5. Describe the forEach and map methods in JavaScript. How are they similar, and
// how do they differ?
//(i) forEach() method:
// Purpose:It executes a function for each element in the array but does not return a new array.
// Mutability: It does not return anything (undefined), meaning it does not create a new array. 


// (ii) map() method:
//  Purpose: It applies a function to each element and returns a new array with transformed values.
// Immutability: The original array remains unchanged, and a new array is created.

// Similarities
//  Both iterate over arrays.
//  Both take a callback function that runs on each element.

// Differences
// forEach() does not create a new array while map() creates a new array.

